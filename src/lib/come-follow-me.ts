/**
 * Returns the CFM week that corresponds to today's date.
 * Falls back to the last week if the date is past the schedule.
 */
export function getCurrentCfmWeek(): CfmWeek {
  const now = new Date();
  // ISO week number (1-53)
  const startOfYear = new Date(now.getFullYear(), 0, 1);
  const weekNumber = Math.ceil(
    ((now.getTime() - startOfYear.getTime()) / 86400000 + startOfYear.getDay() + 1) / 7
  );

  const found = CFM_2026.find((w) => w.week === weekNumber);
  return found ?? CFM_2026[CFM_2026.length - 1];
}
